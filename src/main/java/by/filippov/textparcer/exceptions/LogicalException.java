package by.filippov.textparcer.exceptions;

public class LogicalException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5582515707401932806L;

	public LogicalException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LogicalException(String arg0) {
		super(arg0);
	}

	public LogicalException(Throwable arg0) {
		super(arg0);
	}

}
